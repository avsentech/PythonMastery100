# Code #8: Coundown with While Loop
# Tier: Basic
# Goal: Print countdown from 5 to 1

count = 5
while count > 0:
    print("Countdown:", count)
    count -= 1

# Concept Breakdown
"""
    Concept         Description
    ----------------------------
    While Loop      Runs as long as condition is true
    Decrement       Decreases value step-by-step
"""

# How the Logic Works
"""
    - Start with count = 5
    - Print count and reduce it each time
    - Loop stops when count reaches 0
"""

# Flowchart-Like Visualization
"""
    [Initialize count = 5]
        ↓
    WHILE count > 0:
        → Print count
        → count -= 1
        ↓
    End loop
    [End]
"""

